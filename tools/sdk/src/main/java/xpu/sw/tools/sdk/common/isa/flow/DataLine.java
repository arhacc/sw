//-------------------------------------------------------------------------------------
import java.io.*;
import java.util.*;

import org.apache.commons.lang3.*;
import org.apache.logging.log4j.*;


//-------------------------------------------------------------------------------------
public class DataLine implements Serializable {
    private long data;

//-------------------------------------------------------------------------------------
    public DataLine(String _line) {
        this(Long.parseLong(_line, 16));
    }
    
//-------------------------------------------------------------------------------------
    public DataLine(long _data) {
        data = _data;
    }
    
//-------------------------------------------------------------------------------------
    public long toBin() {
        return data;
    }

//-------------------------------------------------------------------------------------
    public String toHex() {
//        String _s = Long.toHexString(data);
        String _s = String.format("%016X", data);
        _s = _s.toUpperCase();
        _s = _s.substring(0, 8) + "_" + _s.substring(8);
        return _s;
    }

//-------------------------------------------------------------------------------------
    public String toString() {
        return toHex();
    }

//-------------------------------------------------------------------------------------
}
