//-------------------------------------------------------------------------------------
public class PathResolver {

    public static final String HOME = System.getenv("HOME");
    public static final String XPU_LIBRARIES = resolve(System.getenv("XPU_LIBRARIES_PATH"));

//-------------------------------------------------------------------------------------
    public static String resolve(String _path) {
        _path = _path.replace("~", HOME);
        return _path;
    }

//-------------------------------------------------------------------------------------
    
//-------------------------------------------------------------------------------------
}
